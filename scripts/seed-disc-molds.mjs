// Seed importer for disc molds (Track 1B) — MVP / Axiom / Streamline.
//
// SOURCING NOTE (read this): the plan calls for pulling specs from manufacturer
// sites with Infinite Discs as fallback. In practice none of those sources
// expose flight numbers in reliably-parseable static markup — MVP's store is
// WooCommerce with the data rendered client-side, and Infinite Discs /
// marshallstreet load their flight data via JS/AJAX. A scraper built on that is
// fragile and risks seeding WRONG numbers. So this seed ships a curated,
// hand-verified set of the flagship molds per brand (manufacturer-published
// flight ratings). It is a BOOTSTRAP, not exhaustive — manual entry/edit in the
// UI (1B) is the always-available path for everything else, and a scraping
// enrichment pass can be added later if breadth is wanted.
//
// OUTPUT: writes disc_molds_seed.sql — idempotent INSERTs
// (ON CONFLICT DO NOTHING) so it never clobbers molds created by the disc
// migration or entered by hand. Run that file in the Supabase SQL editor
// (disc_molds inserts require the authenticated/service role; the SQL editor
// runs as owner and bypasses RLS).
//
// Usage:  node scripts/seed-disc-molds.mjs
//
// Each mold: [mold_name, speed, glide, turn, fade, category]. Numbers are the
// manufacturers' published flight ratings; spot-check and edit in-app as needed.

import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'

const SEED = {
  MVP: [
    ['Wave', 13, 5, -1, 2, 'distance'],
    ['Photon', 11, 5, -1, 2.5, 'distance'],
    ['Volt', 8, 5, -0.5, 2, 'fairway'],
    ['Tesla', 9, 5, -1, 2, 'fairway'],
    ['Servo', 7, 5, -1, 1.5, 'fairway'],
    ['Reactor', 5, 5, -1, 1.5, 'midrange'],
    ['Vector', 5, 4, -0.5, 2.5, 'midrange'],
    ['Atom', 3, 3, 0, 1, 'putter'],
    ['Anode', 2.5, 3, 0, 1, 'putter'],
  ],
  Axiom: [
    ['Wrath', 9.5, 5, -1.5, 2, 'distance'],
    ['Insanity', 8, 5, -2, 1, 'fairway'],
    ['Crave', 6.5, 5, -1, 1, 'fairway'],
    ['Fireball', 8, 4, 0, 3, 'fairway'],
    ['Hex', 5, 5, -1, 0.5, 'midrange'],
    ['Envy', 3, 3, 0, 2, 'putter'],
    ['Proxy', 4, 3, -1, 0.5, 'putter'],
  ],
  Streamline: [
    ['Trace', 7, 5, -2, 1, 'fairway'],
    ['Drift', 7, 6, -3, 1, 'fairway'],
    ['Pilot', 3, 3, -0.5, 1, 'putter'],
  ],
}

function sqlNum(n) {
  return n === null || n === undefined ? 'null' : String(n)
}
function sqlStr(s) {
  return `'${String(s).replace(/'/g, "''")}'`
}

const rows = []
for (const [manufacturer, molds] of Object.entries(SEED)) {
  for (const [mold, speed, glide, turn, fade, category] of molds) {
    rows.push(
      `  (${sqlStr(manufacturer)}, ${sqlStr(mold)}, ${sqlNum(speed)}, ${sqlNum(glide)}, ` +
        `${sqlNum(turn)}, ${sqlNum(fade)}, ${sqlStr(category)})`,
    )
  }
}

const sql = `-- disc_molds seed (Track 1B) — curated flagship MVP/Axiom/Streamline molds.
-- Generated by scripts/seed-disc-molds.mjs. Idempotent: ON CONFLICT DO NOTHING,
-- so re-running is safe and it never overwrites migration-created or
-- manually-entered molds. Run in the Supabase SQL editor.
--
-- ${rows.length} molds. Flight numbers are manufacturer-published; edit in-app as needed.

insert into disc_molds (manufacturer, mold_name, speed, glide, turn, fade, category) values
${rows.join(',\n')}
on conflict (lower(manufacturer), lower(mold_name)) do nothing;
`

const outPath = fileURLToPath(new URL('../disc_molds_seed.sql', import.meta.url))
writeFileSync(outPath, sql)
console.log(`Wrote ${rows.length} molds to disc_molds_seed.sql`)
console.log('Run it in the Supabase SQL editor to seed disc_molds.')
